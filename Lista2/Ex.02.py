#2. Faça um algoritmo para ler dois números e imprimir se eles são iguais, caso contrário, imprimir a mensagem: “os números são diferentes”
#Resposta:

numeroA = int(input('Digite o número de A: '))
numeroB = int(input('Digite o número de B: '))

if numeroA == numeroB:
  print('Os números são iguais') 
else:
  print('Os números são diferentes')